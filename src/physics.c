void bkp_PhysicsStoping(Agent *a)
{
	if(a->state == BKP_STATE_WALKING)
	{
		a->anim_delay += 2;
		if(a->anim_delay > a->anim_delay_)
		{
			a->state = BKP_STATE_IDLE;
			a->anim_delay = a->delays.d_idle;
			a->aimg->sprite_row = a->sprite_row;
		}
	}

	return;
}

